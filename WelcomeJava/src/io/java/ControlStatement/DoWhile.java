package io.java.ControlStatement;

public class DoWhile {
    public static void main(String[] args) {
        // Initialize counter variable
        int i = 0;
        do{
            System.out.println("Print Statement");
            i++;
        }
        //Checking condition
        //  It is being checked after
        // minimum one iteration
        while(i<0);
    }
}
